const STORAGE_KEY = "mortigenLandingLang";
const SUPPORTED_LANGS = ["en", "es"];

function translatePage(lang) {
  const copy = HOME_TRANSLATIONS[lang] || HOME_TRANSLATIONS.en;

  document.documentElement.lang = lang;
  document.title = copy["meta.title"];

  const description = document.getElementById("page-description");
  if (description) {
    description.setAttribute("content", copy["meta.description"]);
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (copy[key]) {
      element.textContent = copy[key];
    }
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const key = element.dataset.i18nAlt;
    if (copy[key]) {
      element.setAttribute("alt", copy[key]);
    }
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    const key = element.dataset.i18nAriaLabel;
    if (copy[key]) {
      element.setAttribute("aria-label", copy[key]);
    }
  });

  document.querySelectorAll("[data-i18n-href]").forEach((element) => {
    const key = element.dataset.i18nHref;
    if (copy[key]) {
      element.setAttribute("href", copy[key]);
    }
  });

  document.querySelectorAll("[data-i18n-placeholder-label]").forEach((element) => {
    const key = element.dataset.i18nPlaceholderLabel;
    if (copy[key]) {
      element.setAttribute("data-placeholder-label", copy[key]);
    }
  });

  document.querySelectorAll("[data-lang-toggle]").forEach((button) => {
    button.setAttribute(
      "aria-pressed",
      String(button.dataset.langToggle === lang)
    );
  });
}

function persistLanguage(lang) {
  try {
    window.localStorage.setItem(STORAGE_KEY, lang);
  } catch {
    // Ignore storage failures in restricted browsing contexts.
  }
}

function readStoredLanguage() {
  try {
    return window.localStorage.getItem(STORAGE_KEY);
  } catch {
    return null;
  }
}

function updateUrlLanguage(lang) {
  const url = new URL(window.location.href);
  url.searchParams.set("lang", lang);
  window.history.replaceState({}, "", `${url.pathname}${url.search}${url.hash}`);
}

function resolveInitialLanguage() {
  const urlLanguage = new URL(window.location.href).searchParams.get("lang");
  if (SUPPORTED_LANGS.includes(urlLanguage)) {
    return urlLanguage;
  }

  const storedLanguage = readStoredLanguage();
  if (SUPPORTED_LANGS.includes(storedLanguage)) {
    return storedLanguage;
  }

  return navigator.language.toLowerCase().startsWith("es") ? "es" : "en";
}

function initializeLanguageSwitch() {
  const initialLanguage = resolveInitialLanguage();
  translatePage(initialLanguage);
  persistLanguage(initialLanguage);
  updateUrlLanguage(initialLanguage);

  document.querySelectorAll("[data-lang-toggle]").forEach((button) => {
    button.addEventListener("click", () => {
      const nextLanguage = button.dataset.langToggle;
      if (!SUPPORTED_LANGS.includes(nextLanguage)) {
        return;
      }

      translatePage(nextLanguage);
      persistLanguage(nextLanguage);
      updateUrlLanguage(nextLanguage);
    });
  });
}

document.addEventListener("DOMContentLoaded", initializeLanguageSwitch);
